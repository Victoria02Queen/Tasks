package com.company.classsss;

public class Helicopter extends Transport {
    int windSize;
    int motorSize;
    int bladeSize;

    public int getWindSize() {
        return windSize;
    }

    public void setWindSize(int windSize) {
        this.windSize = windSize;
    }

    public int getMotorSize() {
        return motorSize;
    }

    public void setMotorSize(int motorSize) {
        this.motorSize = motorSize;
    }

    public int getBladeSize() {
        return bladeSize;
    }

    public void setBladeSize(int bladeSize) {
        this.bladeSize = bladeSize;
    }
}

